package operators;

public class relational {
    public static void main(String[] args) {
        int a = 40; int b = 30;
        //Equal
       System.out.println(a == b);
       //notequal
       System.out.println(a != b);
       //greaterthan  
       System.out.println(a > b);
       //lesserthan  
       System.out.println(a < b);
       //greater than or equal 
       System.out.println(a >= b);
       //lesser than or equal
       System.out.println(a <= b);  
    }
    
    
}
