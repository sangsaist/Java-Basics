package var_datatype;
//Program to Store and Print Different Data Types
public class primitive {
    static int age = 67;
    static double height = 6.7;
    static char section = 'B';
    static boolean areyoufine = true;

    public static void main(String[] args) {
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("Section: " + section);
        System.out.println("Is Java Fun? " + areyoufine);
    }
}
